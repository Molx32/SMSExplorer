CREATE DATABASE fileexposer;

/* Files */
DROP TABLE IF EXISTS DATA;
DROP TABLE IF EXISTS SMSS;

/* SMS */
CREATE TABLE SMSS(
    ID SERIAL PRIMARY KEY,
    SENDER VARCHAR(100),
    RECEIVER VARCHAR(100),
    MSG VARCHAR(5000),
    RECEIVE_DATE TIMESTAMP,
    COUNTRY VARCHAR(100),
    URL VARCHAR(1000),
    DOMAIN VARCHAR(500),
    SOURCE VARCHAR(200),
    DATA_HANDLED BOOLEAN NOT NULL
);

CREATE TABLE DATA(
    ID SERIAL PRIMARY KEY,
    SMS_DATA VARCHAR(32000),
    SMS_ID INT REFERENCES SMSS(ID)
);


ALTER DATABASE fileexposer SET datestyle TO 'GERMAN,MDY';