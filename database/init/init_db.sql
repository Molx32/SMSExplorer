CREATE DATABASE fileexposer;

/* Files */
DROP TABLE IF EXISTS SMSS;

/* SMS */
CREATE TABLE SMSS(
    ID SERIAL PRIMARY KEY,
    SENDER VARCHAR(100),
    RECEIVER VARCHAR(100),
    MSG VARCHAR(5000),
    RECEIVE_DATE TIMESTAMP,
    COUNTRY VARCHAR(100),
    INSTAGRAM_ACC VARCHAR(500),
    URL VARCHAR(1000),
    DOMAIN VARCHAR(500),
    SOURCE VARCHAR(200)
);

